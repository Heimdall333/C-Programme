#include<stdio.h>
int main()
{
	int x=33;
	printf("The size of variable x is :%d\n",sizeof(x)); 
	printf("The size of int datatype is :%d\n",sizeof(int));
	printf("The size of float datatype is :%d\n",sizeof(float));
	printf("The size of character datatype is :%d\n",sizeof(char));
	
	return 0;
}
